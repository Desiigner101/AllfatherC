//Arithmetic operators-------------------------------------------------------------------------------------------------

#include <stdio.h>
#include <stdbool.h>

int main(){

    // + (additio)
    // - (subtraction)
    // * (multiplication)
    // / (division)
    // % (modulus)
    // ++ (increment)
    // -- (decrement)

    int x = 5;
    int y = 2;

    float z = x / (float) y;
    x++;
    y--;

    printf ("%.1f",z);

    return 0;

}   
